	</div>
</template>
<script>
	import store from '@/store'
	import modal from '@/components/modal'
	import tabs from '@/components/tabs.vue'
	import qrCodeGenerator from '@/components/qrCodeGenerator.vue'
	import urlDisplay from '@/components/urlDisplay.vue'
	import translations from '@/functions/translations.js'
	import api from '@/functions/apiFunctions.js'
	import f from '@/functions/functions.js'
	export default {
		name: 'appFooter',
		data () {
			return {
				store: store,
				selectedTab: 0,
				showShareModal: false,
				showQrModal: false,
				showUrlModal: false,
				showImageModal: false,
				footerPages: ['front', 'addEvent', 'settings'],
				actions: [this.front, this.addEvent, this.settings, this.share],
			}
		},
		components: {
			modal,
			tabs,
			qrCodeGenerator,
			urlDisplay,
		},
		props: {
		},
		computed: {
			isAuthenticatedUser () {
				return f.isAuthenticatedUser
			},
		},
		mounted () {
			this.$emit('endLoading')
		},
		watch: {
		},
		methods: {
			t (w) { return translations.t(w) },
			selectTab (selectedTab) {
				this.actions[selectedTab-1]()
			},
			front () {
				this.$emit('modalPage', 'front', null)
			},
			addEvent () {
				if(this.isAuthenticatedUser) {
					this.$emit('modalPage', 'addEvent', null)
				} else if (this.store.path != '/?page=loginRegister') {
					this.$emit('modalPage', 'loginRegister', null)
				}
			},
			settings () {
				console.log(this.store.path)
				if(this.isAuthenticatedUser) {
					this.$emit('modalPage', 'settings', null)
				} else if (this.store.path != '/?page=loginRegister') {
					this.$emit('modalPage', 'loginRegister', null)
				}
			},
			share () {
				this.showShareModal = true
			},
		}
	}
